# Operadores Matemáticos em Ruby

# Ruby suporta uma variedade de operadores matemáticos que permitem realizar cálculos matemáticos.

# Operadores aritméticos:
# - Adição (+): Soma dois valores.
# - Subtração (-): Subtrai o valor à direita do valor à esquerda.
# - Multiplicação (*): Multiplica dois valores.
# - Divisão (/): Divide o valor à esquerda pelo valor à direita.
# - Módulo (%): Retorna o resto da divisão.
# - Exponenciação (**): Eleva o valor à esquerda à potência do valor à direita.

# Exemplos:
soma = 5 + 3  # Isso atribuirá 8 à variável 'soma'.
subtracao = 10 - 4  # Isso atribuirá 6 à variável 'subtracao'.
multiplicacao = 6 * 2  # Isso atribuirá 12 à variável 'multiplicacao'.
divisao = 15 / 3  # Isso atribuirá 5 à variável 'divisao'.
modulo = 10 % 3  # Isso atribuirá 1 à variável 'modulo'.
exponenciacao = 2**3  # Isso atribuirá 8 à variável 'exponenciacao'.

# Operadores de atribuição:
# - Atribuição (=): Atribui um valor a uma variável.
# - Atribuição com adição (+=): Adiciona um valor a uma variável existente.
# - Atribuição com subtração (-=): Subtrai um valor de uma variável existente.
# - Atribuição com multiplicação (*=): Multiplica uma variável existente por um valor.
# - Atribuição com divisão (/=): Divide uma variável existente por um valor.
# - Atribuição com módulo (%=): Calcula o módulo e atribui a uma variável existente.

# Exemplos:
numero = 10  # Isso atribuirá 10 à variável 'numero'.
numero += 5  # Isso adicionará 5 a 'numero', resultando em 15.
numero -= 3  # Isso subtrairá 3 de 'numero', resultando em 12.
numero *= 4  # Isso multiplicará 'numero' por 4, resultando em 48.
numero /= 2  # Isso dividirá 'numero' por 2, resultando em 24.
numero %= 7  # Isso calculará o módulo de 'numero' por 7, resultando em 3.

# Operadores de incremento e decremento:
# - Incremento (++) e Decremento (--): Aumentam ou diminuem o valor de uma variável em 1.

# Exemplos:
contador = 0  # Inicializa 'contador' com 0.
contador++  # Isso aumenta 'contador' para 1.
contador--  # Isso diminui 'contador' para 0 novamente.

# Ruby oferece uma variedade de operadores matemáticos para realizar cálculos e manipular valores numéricos.
