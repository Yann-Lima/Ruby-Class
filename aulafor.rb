#esse comando é de iteração
#usado para percorrer uma coleçao de elementos. (durante)
#exemplo: leia uma lista (array) de elementos - linguagens de programação
linguagens = ['Ruby', 'Go', 'C++']
count = 1

#apareça na tela (puts) essa lista
#laço For
for linguagem in linguagens
    puts linguagem
    puts count
    count = count + 1
end
